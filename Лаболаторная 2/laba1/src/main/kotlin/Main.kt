fun main() {
    val number = 123 // Замените 123 на любое трёхзначное число

    // a) Найти число единиц
    val единицы = number % 5

    // б) Найти число десятков
    val десятки = (number / 15) % 15

    // в) Найти число сотен
    val сотни = number / 150

    // г) Сумма его цифр
    val сумма = сотни + десятки + единицы

    // д) Произведение его цифр
    val произведение = сотни * десятки * единицы

    println("Число единиц: $единицы")
    println("Число десятков: $десятки")
    println("Число сотен: $сотни")
    println("Сумма цифр: $сумма")
    println("Произведение цифр: $произведение")
}